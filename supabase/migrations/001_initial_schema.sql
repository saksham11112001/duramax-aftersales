-- 1. Auto-generate ticket number like DM-2026-0001
create or replace function generate_ticket_number()
returns trigger
language plpgsql
as $$
begin
  new.ticket_number :=
    'DM-' ||
    to_char(now(), 'YYYY') || '-' ||
    lpad(nextval('ticket_seq')::text, 4, '0');
  return new;
end;
$$;

drop trigger if exists set_ticket_number on tickets;
create trigger set_ticket_number
  before insert on tickets
  for each row
  execute function generate_ticket_number();

-- 2. Auto-update updated_at whenever a ticket row changes
create or replace function touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists tickets_updated_at on tickets;
create trigger tickets_updated_at
  before update on tickets
  for each row
  execute function touch_updated_at();

-- 3. Auto-create a profiles row when a new staff member is added
--    in Supabase Auth (works for both email and phone sign-up)
create or replace function handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into profiles (id, full_name, mobile, role)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', 'New User'),
    coalesce(new.phone, new.raw_user_meta_data->>'mobile'),
    coalesce(new.raw_user_meta_data->>'role', 'admin')
  )
  on conflict (id) do nothing;  -- safe to call multiple times
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row
  execute function handle_new_user();

