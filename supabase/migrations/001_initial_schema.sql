alter table profiles               enable row level security;
alter table tickets                enable row level security;
alter table payments               enable row level security;
alter table supervisor_allocations enable row level security;
alter table site_visits            enable row level security;
alter table spare_parts            enable row level security;
alter table feedback               enable row level security;
alter table notification_log       enable row level security;

-- Helper function: returns the role of the currently logged-in user
-- without causing a recursive RLS check.
create or replace function current_user_role()
returns text
language sql
security definer stable
as $$
  select role from profiles where id = auth.uid();
$$;

-- ── PROFILES policies ──
-- Each user reads their own profile
create policy "read own profile"
  on profiles for select
  using (id = auth.uid());

-- Admin can read and manage all profiles
create policy "admin manages profiles"
  on profiles for all
  using (current_user_role() = 'admin');

-- ── TICKETS policies ──
-- Anyone (including unauthenticated) can submit a new ticket
create policy "public can submit ticket"
  on tickets for insert
  with check (true);

-- Anyone can read a ticket to support tracking
-- (the actual mobile verification is done in the API route, not RLS)
create policy "public can read ticket for tracking"
  on tickets for select
  using (true);

-- Admin and accounts can update any ticket
create policy "admin accounts update tickets"
  on tickets for update
  using (current_user_role() in ('admin', 'accounts'));

-- Supervisor can read tickets assigned to them
create policy "supervisor reads own tickets"
  on tickets for select
  using (
    exists (
      select 1 from supervisor_allocations sa
      where sa.ticket_id = tickets.id
        and sa.supervisor_id = auth.uid()
    )
  );

-- ── PAYMENTS policies ──
-- Public can read a payment row by token (for the pay page)
-- Actual token validation done in API route
create policy "public reads payment by token"
  on payments for select
  using (true);

-- Admin and accounts can manage all payments
create policy "admin accounts manage payments"
  on payments for all
  using (current_user_role() in ('admin', 'accounts'));

-- ── SUPERVISOR ALLOCATIONS policies ──
create policy "admin manages supervisor allocations"
  on supervisor_allocations for all
  using (current_user_role() = 'admin');

create policy "supervisor reads own allocation"
  on supervisor_allocations for select
  using (supervisor_id = auth.uid());

-- ── SITE VISITS policies ──
create policy "admin accounts manage visits"
  on site_visits for all
  using (current_user_role() in ('admin', 'accounts'));

create policy "supervisor manages own visits"
  on site_visits for all
  using (supervisor_id = auth.uid());

-- ── SPARE PARTS policies ──
create policy "admin accounts manage parts"
  on spare_parts for all
  using (current_user_role() in ('admin', 'accounts'));

create policy "supervisor manages own parts"
  on spare_parts for all
  using (
    exists (
      select 1 from site_visits sv
      where sv.id = site_visit_id
        and sv.supervisor_id = auth.uid()
    )
  );

-- ── FEEDBACK policies ──
-- Anyone can submit feedback via token (validated in API route)
create policy "public submits feedback"
  on feedback for insert
  with check (true);

create policy "public reads feedback token"
  on feedback for select
  using (true);

create policy "admin updates feedback"
  on feedback for update
  using (current_user_role() in ('admin', 'accounts'));

-- ── NOTIFICATION LOG policies ──
create policy "admin reads notification log"
  on notification_log for select
  using (current_user_role() in ('admin', 'accounts'));

create policy "service inserts notification log"
  on notification_log for insert
  with check (true);

