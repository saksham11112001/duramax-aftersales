-- Auto-incrementing counter for readable ticket numbers
create sequence if not exists ticket_seq start 1;

-- ── TABLE 1: PROFILES ────────────────────────────────────────
-- One row per staff member. Created automatically when a user
-- signs up via Supabase Auth (see trigger at the bottom).
create table if not exists profiles (
  id          uuid references auth.users on delete cascade primary key,
  full_name   text not null default 'New User',
  mobile      text,
  role        text not null default 'installer'
                check (role in ('admin', 'supervisor', 'accounts')),
  is_active   boolean not null default true,
  created_at  timestamptz not null default now()
);

-- ── TABLE 2: TICKETS ─────────────────────────────────────────
-- Every customer service request is one row here.
-- This is the central table everything else links to.
create table if not exists tickets (
  id                    uuid not null default gen_random_uuid() primary key,

  -- Human-readable ID like DM-2026-0001 (auto-generated by trigger)
  ticket_number         text unique,

  -- Customer details (no account, just data)
  client_name           text not null,
  client_mobile         text not null,  -- also used for tracking verification
  site_address          text not null,
  complaint_description text not null,
  brand_installed       text,
  duromax_installation  boolean,
  preferred_slot        text,           -- 'morning' | 'afternoon' | 'evening'
  photo_url             text,           -- optional photo from Supabase Storage

  -- Pricing flag set by admin when raising invoice
  is_outstation         boolean not null default false,

  -- The core status field — drives everything
  status  text not null default 'new'
    check (status in (
      'new',              -- customer submitted the form
      'invoiced',         -- admin raised ₹3,000 visit fee invoice
      'paid',             -- visit fee paid via Razorpay
      'scheduled',        -- admin assigned a supervisor
      'visited',          -- supervisor submitted site report
      'parts_invoiced',   -- admin raised spare parts invoice
      'parts_paid',       -- customer paid spare parts invoice
      'closed'            -- admin marked ticket as done
    )),

  created_at  timestamptz not null default now(),
  updated_at  timestamptz not null default now()
);

-- ── TABLE 3: PAYMENTS ────────────────────────────────────────
-- Tracks both the ₹3,000 visit fee AND the spare parts payment.
-- Each gets its own row with its own signed token for the pay link.
create table if not exists payments (
  id                    uuid not null default gen_random_uuid() primary key,
  ticket_id             uuid not null references tickets on delete cascade,

  -- visit_fee = first payment | spare_parts = second payment
  payment_type          text not null check (payment_type in ('visit_fee', 'spare_parts')),

  -- UUID token for the /pay/[token] URL. One-time, expires in 48h.
  token                 uuid not null default gen_random_uuid() unique,
  token_expires_at      timestamptz not null default (now() + interval '48 hours'),

  -- Razorpay fields — filled when order is created and webhook fires
  razorpay_order_id     text,
  razorpay_payment_id   text,           -- filled by webhook on success

  -- Amount stored in paise (₹3,000 = 300000, ₹4,500 = 450000)
  amount_paise          integer not null,

  status  text not null default 'pending'
    check (status in ('pending', 'paid', 'failed', 'expired')),

  paid_at     timestamptz,              -- filled by webhook
  created_at  timestamptz not null default now()
);

-- ── TABLE 4: SUPERVISOR ALLOCATIONS ──────────────────────────
create table if not exists supervisor_allocations (
  id              uuid not null default gen_random_uuid() primary key,
  ticket_id       uuid not null references tickets on delete cascade unique,
  supervisor_id   uuid not null references profiles,
  allocated_by    uuid not null references profiles,  -- the admin who assigned
  visit_date      date not null,
  time_slot       text not null,        -- e.g. '10:00–12:00 AM'
  notes           text,                 -- optional notes for supervisor
  allocated_at    timestamptz not null default now(),

  -- SLA: admin must assign within 48h of payment
  sla_deadline    timestamptz not null default (now() + interval '48 hours'),
  sla_alerted     boolean not null default false  -- true once breach email sent
);

-- ── TABLE 5: SITE VISITS ─────────────────────────────────────
-- Supervisor fills this form during their on-site visit.
create table if not exists site_visits (
  id                  uuid not null default gen_random_uuid() primary key,
  ticket_id           uuid not null references tickets on delete cascade unique,
  supervisor_id       uuid not null references profiles,
  visit_date          date not null,
  client_complaint    text not null,    -- what the customer told them
  observed_issue      text not null,    -- what the supervisor actually found
  urgency_level       text default 'medium' check (urgency_level in ('low', 'medium', 'high')),
  est_repair_time     text,             -- free text, e.g. '2–3 hours'
  warranty_status     text,             -- 'within' | 'expired' | 'not_duromax'
  supervisor_signed   boolean not null default false,
  client_signed       boolean not null default false,
  remarks             text,
  submitted_at        timestamptz not null default now()
);

-- ── TABLE 6: SPARE PARTS ─────────────────────────────────────
-- Each row is one part in the site visit report.
-- Child of site_visits.
create table if not exists spare_parts (
  id              uuid not null default gen_random_uuid() primary key,
  site_visit_id   uuid not null references site_visits on delete cascade,
  s_no            integer not null,         -- row number (1, 2, 3…)
  article_name    text not null,
  article_number  text,
  quantity        integer not null default 1,
  unit_price_paise integer,                 -- optional, for invoice calculation
  remarks         text
);

-- ── TABLE 7: FEEDBACK ────────────────────────────────────────
-- Optional post-closure feedback from customer.
-- Has its own token so the link can be sent via WhatsApp without login.
create table if not exists feedback (
  id                   uuid not null default gen_random_uuid() primary key,
  ticket_id            uuid not null references tickets on delete cascade unique,

  -- UUID token for /feedback/[token] URL
  token                uuid not null default gen_random_uuid() unique,
  token_used           boolean not null default false,  -- prevent double submit

  -- Ratings all 1–5
  overall_rating       integer check (overall_rating between 1 and 5),
  supervisor_rating    integer check (supervisor_rating between 1 and 5),
  quality_rating       integer check (quality_rating between 1 and 5),
  timeliness_rating    integer check (timeliness_rating between 1 and 5),
  comment              text,
  submitted_at         timestamptz
);

-- ── TABLE 8: NOTIFICATION LOG ────────────────────────────────
-- Every WhatsApp message and email we send gets logged here.
-- Useful for debugging and re-send logic.
create table if not exists notification_log (
  id              uuid not null default gen_random_uuid() primary key,
  ticket_id       uuid references tickets on delete set null,
  channel         text not null check (channel in ('whatsapp', 'email', 'sms')),
  recipient       text not null,        -- phone number or email address
  template_name   text not null,        -- e.g. 'invoice_raised'
  status          text not null default 'sent' check (status in ('sent', 'failed')),
  error_message   text,                 -- filled if status = 'failed'
  sent_at         timestamptz not null default now()
);

